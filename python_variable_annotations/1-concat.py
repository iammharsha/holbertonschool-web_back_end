#!/usr/bin/env python3
"""Module to concat two strings"""


def concat(str1: str, str2: str) -> str:
    """
    Concat two strings and return the result.

    Args:
        str1 (string): The first string.
        str2 (string): The second string.

    Returns:
        string: The concatinated string.
    """
    return str1 + str2
